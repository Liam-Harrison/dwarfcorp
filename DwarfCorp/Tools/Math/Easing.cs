        {
            if ((t /= d / 2) < 1)
                return (float) (-c / 2 * (Math.Sqrt(1 - t * t) - 1) + b);

            return (float) (c / 2 * (Math.Sqrt(1 - (t -= 2) * t) + 1) + b);
        }

        /// <summary>
        /// Easing equation function for a circular (sqrt(1-t^2)) easing in/out: 
